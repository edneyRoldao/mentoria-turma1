// declarando e definindo uma lista
//           0        1        2       3        (index)
let list = ['Edney', 'Maria', 'Joao', 'Rebecca'] // minha tem 4 elementos

// como acessar o item de um array
console.log(list)
console.log(list[3])

// adicionando um novo elemento na lista (no final da lista)
list.push('Marjorie')

// removendo um elemento da lista (no final da lista)
list.pop()

// adicionando um novo elemento no inicio da lista
list.unshift('Ken')

// removendo um elemento do inicio da lista
list.shift()

// como verificar o tamanho de um array
console.log('exibindo tamanho da lista:', list.length);


console.log(list)
