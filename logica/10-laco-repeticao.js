/* 
    ESTRUTURA REPETICAO - WHILE (enquanto)
*/

// incremento/decremento, condicao de parada.

let n = 1
while /* enquanto */ (n < 10) {
    console.log('esse e um bloco de codigo que esta em um laco  de repeticao while', n);
    n++
}

console.log('fim do laco');
// condicao de parada: n < 10
// incremento: n++